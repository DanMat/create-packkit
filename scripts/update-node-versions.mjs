#!/usr/bin/env node
// Derive the Node versions we offer + pin from Node's OWN release data, so we
// never guess a patch (e.g. "20.19.0") or hand-track which line is LTS.
//
//   - which majors to offer  -> the Node release schedule (Active LTS,
//     Maintenance LTS, Current), so EOL lines drop off automatically and new
//     lines appear as they ship.
//   - the exact patch to pin -> the newest release of each line from the dist
//     index (this is how we get e.g. 22.23.1 without guessing).
//
// Writes src/core/node-versions.js (a committed, browser-safe data module).
// A weekly workflow re-runs this so the defaults track upstream.

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

// Minimum Node *within a major* our template deps need. Keep in sync with
// ENGINE_MIN in src/core/node.js — kept inline here so this build tool never
// depends on the file it generates.
const ENGINE_MIN = { 18: '18.18.0', 20: '20.19.0', 22: '22.13.0' };

const DIST = 'https://nodejs.org/dist/index.json';
const SCHEDULE = 'https://raw.githubusercontent.com/nodejs/Release/main/schedule.json';

const meetsFloor = (version, floor) => {
  const p = (s) => String(s).replace(/^v/, '').split('.').map((n) => parseInt(n, 10) || 0);
  const [a, b, c] = p(version);
  const [x, y, z] = p(floor);
  if (a !== x) return a > x;
  if (b !== y) return b > y;
  return c >= z;
};

const [dist, schedule] = await Promise.all([
  fetch(DIST).then((r) => r.json()),
  fetch(SCHEDULE).then((r) => r.json()),
]);

// Newest published patch for each major, straight from Node.
const latestPatch = {};
for (const rel of dist) {
  const major = Number(rel.version.slice(1).split('.')[0]);
  if (!(major in latestPatch)) latestPatch[major] = rel.version.slice(1);
}

// Status for each major from the official schedule, as of today.
const now = new Date();
const at = (d) => (d ? new Date(d) : null);
const statusOf = (s) => {
  if (at(s.end) && now > at(s.end)) return 'eol';
  if (at(s.maintenance) && now >= at(s.maintenance)) return 'maintenance';
  if (at(s.lts) && now >= at(s.lts)) return 'active-lts';
  if (at(s.start) && now >= at(s.start)) return 'current';
  return 'pending';
};

const LABEL = { 'active-lts': 'Active LTS', maintenance: 'Maintenance LTS', current: 'Current' };
const RANK = { 'active-lts': 0, maintenance: 1, current: 2 }; // recommended order

const lines = [];
for (const [key, s] of Object.entries(schedule)) {
  const major = Number(key.replace(/^v/, ''));
  const status = statusOf(s);
  if (!(status in LABEL)) continue; // skip eol / pending
  const version = latestPatch[major];
  if (!version) continue; // no published release yet
  // Only offer a line our template deps can actually run on.
  const floor = ENGINE_MIN[major] || `${major}.0.0`;
  if (!meetsFloor(version, floor)) continue;
  lines.push({ major, version, status, codename: s.codename || null });
}

// Recommended order: Active LTS, Maintenance LTS, then Current. Newest first
// within a status.
lines.sort((a, b) => RANK[a.status] - RANK[b.status] || b.major - a.major);

// The default is DERIVED, never hardcoded: whichever line the schedule marks
// Active LTS today (newest first, since `lines` is sorted). If we're briefly
// between LTS cutovers with no Active LTS, fall back to the best available
// (Maintenance LTS, then Current). This is why the default self-maintains — a
// new Active LTS automatically becomes the default on the next refresh.
const active = lines.find((l) => l.status === 'active-lts') || lines[0];
const DEFAULT_NODE = String(active.major);

const body = lines
  .map((l) => {
    const label = `${l.major} (${LABEL[l.status]}${l.codename ? `, "${l.codename}"` : ''})`;
    return `  '${l.major}': { version: '${l.version}', status: '${l.status}', codename: ${
      l.codename ? `'${l.codename}'` : 'null'
    }, label: '${label}' },`;
  })
  .join('\n');

const out = `// AUTO-GENERATED by scripts/update-node-versions.mjs — do not edit by hand.
// Source: nodejs.org/dist + nodejs/Release schedule, refreshed ${now.toISOString().slice(0, 10)}.
// Offered lines track the Node release schedule; each version is that line's
// newest published patch. Run \`node scripts/update-node-versions.mjs\` to refresh.

export const NODE_LINES = {
${body}
};

export const DEFAULT_NODE = '${DEFAULT_NODE}';
`;

const dest = fileURLToPath(new URL('../src/core/node-versions.js', import.meta.url));
writeFileSync(dest, out);
console.log(`Wrote ${lines.length} Node lines (default ${DEFAULT_NODE}):`);
for (const l of lines) console.log(`  ${l.major}\t${l.version}\t${l.status}${l.codename ? ` (${l.codename})` : ''}`);
